public class GridInfo {
    public String region;
    public int gridX;
    public int gridY;
    public String city;
    public String state;

    public GridInfo(String region, int gridX, int gridY, String city, String state) {
        this.region = region;
        this.gridX = gridX;
        this.gridY = gridY;
        this.city = city;
        this.state = state;
    }
}
